# Uma lista de listas
lista1 = [
    ['a', 1],
    ['b', 2],
    ['c', 3]
]

# Convertendo listas em dicionário
dic = dict(lista1)
print(dic)


tupla = tuple(lista1)
print(tupla)

# Uma lista de tuplas
lista2 = [
    ('a', 1),
    ('b', 2),
    ('c', 3)
]

# Convertendo tuplas em dicionário
dic2 = dict(lista2)
print(dic2)
